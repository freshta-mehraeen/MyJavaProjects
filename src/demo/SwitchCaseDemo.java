package demo;

public class SwitchCaseDemo {
	public static void main(String[] args) {
		String day = "friday";
		
		switch(day) {
		
		
			case "monday":
				System.out.println("wear a yellow dress today");
				break; 
				
				
			case "tuesday":
				System.out.println("wear an orange dress today");
				break; 
				
				
			case "wednesday":
				System.out.println("wear a green dress today");
				break; 
				
				
			case "thursday":
				System.out.println("wear a brown dress today");
				break; 
				
			case "friday":
				System.out.println("wear a silver dress today");
				break; 
				
			case "saturday":
				System.out.println("wear a grey dress today");
				break; 
				
			case "sunday":
				System.out.println("wear a white dress today"); 
		}
				
		} 
} 
 
